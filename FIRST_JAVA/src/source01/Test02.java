package source01;

import java.util.Date;

public class Test02 {
	// Ctrl + Shift + O = 임포트 자동 정리
	// (필요한 임포트는 처리해 주고, 필요없는 임포트는 삭제 처리함)
	// Alt + Shift + Y = 이클립스 자동 줄바꿈
	// Ctrl + S = 저장, Ctrl + Z = 뒤로 가기, Ctrl + Y = 앞으로 가기
	// Ctrl + D = 라인 삭제
	// Alt + ↑ , Alt + ↓ = 라인 Move
	// Ctrl + Alt + ↑ or ↓ = 라인 Copy
	// Ctrl + F11 = 파일 Run 실행
	// Ctrl + L = 특정 줄번호로 커서 이동
	// Ctrl + / = 한줄 주석 설정 및 해제(토글 적용)
	// Ctrl + Shift + / = 블록 주석 설정
	// Ctrl + Shift + \ = 블록 주석 해제
	// Ctrl + Shift + F = 자동 정렬
	// (클래스명 선택 후) Ctrl + T = 상속 관계 표현(상위 슈퍼 타입 확인)
	// Ctrl + + = 폰트 크기 확대
	// Ctrl + - = 폰트 크기 축소
	// Ctrl + Shift + - = 화면 가로 나누기
	// Ctrl + Shift + [ = 화면 세로 나누기
	// Alt + ←, Alt + → = 이전 자바 파일 히스토리 이동	
	public static void main(String[] args) {
		System.out.println("Hello~ JAVA~ ^-^/");
		Date today = new Date();
		System.out.println("Today is " + today);
	}
}
