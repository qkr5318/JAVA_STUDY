package source02.chapter02;

// Escape 이스케이프 문자 출력 예시
public class Test10_EscapeExample {
	public static void main(String[] args) {
		System.out.println("번호\t이름\t직업"); // 번호	이름	직업
		System.out.print("행 단위 출력\n"); // 행 단위 출력
		System.out.println("우리는 \"개발자\" 입니다!"); // 우리는 "개발자" 입니다!
		System.out.print("봄\\여름\\가을\\겨울"); // 봄\여름\가을\겨울
	}
}
