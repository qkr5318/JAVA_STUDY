package source03;

public class Test02_01 {

	public static void main(String[] args) {

		int a = 3, b = 4, c = 5, d = 6;
		
		a += 10;  // a = a + 10;
		System.out.println("a = " + a);
		
		b -= 10;  // b = b - 10;
		System.out.println("b = " + b);
		
		c *= 10;  // c = c * 10;
		System.out.println("c = " + c);
		
		d /= 3;  // d = d / 3;
		System.out.println("d = " + d);
	}
}
